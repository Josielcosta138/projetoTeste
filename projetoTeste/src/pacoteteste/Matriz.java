package pacoteteste;

public class Matriz {

	public static void main(String[] args) {
		
		//Criando uma Matriz multidimensional
		
		int notas[][] = new int[3][3]; 
		
		notas[0][0] = 30;
		notas[0][1] = 53;
		notas[0][2] = 45;
		
		notas[1][0]	= 43;
		
		notas[2][1] = 333;
		
		
		
		System.out.println(notas);
	}
}
